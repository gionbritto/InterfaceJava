    private void jButtonEscolheArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEscolheArquivoActionPerformed
        /*
        JFileChooser e a classe responsavel por abrir o escolhedor de arquivos
        do java
        */
        JFileChooser fileChooser = new JFileChooser(); //instancia normal de JFileChooser
        FileNameExtensionFilter filter = new FileNameExtensionFilter("imagem", "jpg", "png"); //Classe que cria um fitro para extensoes
        
        fileChooser.setDialogTitle("Procurar arquivo");
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY); //metodo que especifica que tipo de restri√ßao vou ter na pesquisa
        fileChooser.setFileFilter(filter);
        int retorno = fileChooser.showOpenDialog(this); //o metodo showOpenDialog abre o dialogo com o sistema e recebe o parent, que neste caso e .this
        
        if(retorno == JFileChooser.APPROVE_OPTION){//verifica a opcao de retorno utilizando a constante da propria classe
            
            File file = fileChooser.getSelectedFile(); // metodo getSelectedFile que pega o caminho do aarquivo selecionado
            jTextFieldCaminhoArq.setText(file.getPath()); //seta o texto para o caminho do arquivo selecionado
            jLabelImagem.setIcon(new ImageIcon(file.getPath()));  /*utilizar o metodo setIcon do label para colocar a imagem no lugar
            correto, com a classe ImageIcon e utilizar a sobrecarga que recebe uma url(location) que 
            por este lado eh o path(caminho) recebido pelo obj file no metodo getPath(); 
            */
        }
        System.out.println(retorno);
        
    }//GEN-LAST:event_jButtonEscolheArquivoActionPerformed

