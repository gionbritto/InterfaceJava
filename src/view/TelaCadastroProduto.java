    private void jTableProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProdutosMouseClicked
       int linhaAExcluir = jTableProdutos.getRowCount();
       DefaultTableModel dtmProdutos = (DefaultTableModel)jTableProdutos.getModel();
       
       if(linhaAExcluir != -1){
          
        jTextFieldDescricao.setText(jTableProdutos.getValueAt(jTableProdutos.getSelectedRow(), 0).toString());
        jTextFieldQtd.setText(jTableProdutos.getValueAt(jTableProdutos.getSelectedRow(), 1).toString());
        jFormattedTextFieldPreco.setText(jTableProdutos.getValueAt(jTableProdutos.getSelectedRow(), 2).toString());
           
           
       }
       
    }//GEN-LAST:event_jTableProdutosMouseClicked

    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        if(jTableProdutos.getSelectedRow() != -1){
          
            jTableProdutos.setValueAt(jTextFieldDescricao.getText(),jTableProdutos.getSelectedRow() , 0);
            jTableProdutos.setValueAt(jTextFieldQtd.getText(),jTableProdutos.getSelectedRow() , 1);
            jTableProdutos.setValueAt(jFormattedTextFieldPreco.getText(),jTableProdutos.getSelectedRow() , 2);
           
           
       }
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

