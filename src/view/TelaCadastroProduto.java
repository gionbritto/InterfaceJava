    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        /*
        O metodo getSelectedRow da classe jTable retorna para nos um 
        numero inteiro informando qual a fileira selecionada, da tabela em
        questao. Caso nao seja selecionada nenhuma opcao sera retornado o valor -1;
        */
        //System.out.println("Linha selecionada: " + jTableProdutos.getSelectedRow());
         
         int linhaAExcluir = jTableProdutos.getSelectedRow(); //guarda o valor da linha selecionada pa exclusao
         DefaultTableModel dtmProdutos = (DefaultTableModel)jTableProdutos.getModel();  //Colocamos a tabela dentro de um tpo dafault
         if(linhaAExcluir == -1){
             
             JOptionPane.showMessageDialog(null, "Nenhuma produto selecionado!","Alerta",JOptionPane.ERROR_MESSAGE); //mostra o dialog caso nao haja nenhuma opcao selecionada
             
         } else {
            int input = JOptionPane.showConfirmDialog(null, "Deseja realmente apagar?" +
                     "\nProduto Cod.: " + linhaAExcluir + " Selecionado!",
                     "Confirmaçao" ,JOptionPane.OK_CANCEL_OPTION); //cancelar retona 2 ok retora 0
            if(input == 0){
                dtmProdutos.removeRow(linhaAExcluir); //exclui a linha selecionada
            }
             System.out.println(input);
         }
            
         
        
    }//GEN-LAST:event_jButtonExcluirActionPerformed

